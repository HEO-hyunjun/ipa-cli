import esbuild from "esbuild";
import { cp, mkdir, readFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { basename, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const watch = process.argv.includes("--watch");
const copyDev = process.argv.includes("--copy-dev");

const spec = JSON.parse(
  await readFile(join(root, "packages", "obsidian", "build-spec.json"), "utf8")
);

const outDir = join(root, spec.outDir);
const releaseAssets = ["main.js", "manifest.json", "styles.css", "versions.json"];

async function copyAssets() {
  await mkdir(outDir, { recursive: true });
  for (const asset of spec.assets) {
    await cp(join(root, asset), join(outDir, basename(asset)));
  }
  if (copyDev && spec.devVaultPluginDir) {
    await mkdir(spec.devVaultPluginDir, { recursive: true });
    for (const file of releaseAssets) {
      const source = join(outDir, file);
      if (existsSync(source)) {
        await cp(source, join(spec.devVaultPluginDir, file));
      }
    }
  }
}

// Copy manifest/styles/versions (and optionally install into the dev vault) on
// every successful build so watch mode keeps the plugin folder in sync.
const assetPlugin = {
  name: "ipa-obsidian-assets",
  setup(build) {
    build.onEnd(async (result) => {
      if (result.errors.length > 0) return;
      await copyAssets();
      console.log(`[build-obsidian] assets synced${copyDev ? " (dev vault)" : ""}`);
    });
  }
};

const options = {
  entryPoints: [join(root, spec.entry)],
  outfile: join(outDir, "main.js"),
  bundle: true,
  format: "cjs",
  target: "es2022",
  platform: "node",
  external: spec.external,
  sourcemap: watch ? "inline" : false,
  logLevel: "info",
  plugins: [assetPlugin],
  banner: {
    js: "/* IPA Obsidian plugin bundle. Generated by scripts/build-obsidian.mjs. Do not edit. */"
  }
};

if (watch) {
  const context = await esbuild.context(options);
  await context.watch();
  console.log("[build-obsidian] watching packages/obsidian/src ...");
} else {
  await esbuild.build(options);
  console.log("[build-obsidian] build complete");
}
